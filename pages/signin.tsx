import AccountCircle from "@mui/icons-material/AccountCircle";
import LockIcon from "@mui/icons-material/Lock";
import Box from "@mui/material/Box";
import Button from "@mui/material/Button";
import Divider from "@mui/material/Divider";
import Typography from "@mui/material/Typography";
import {
  HorizontalEnd,
  MainContainer,
  RegisterFormContainer,
  TextInput,
} from "components";
import { useAppDispatch, useAppSelector } from "hook/redux";
import type { NextPage } from "next";
import Head from "next/head";
import Link from "next/link";
import { setForm } from "store/signin";
import { Header } from "views/Header";

const SignInForm: NextPage<{
  form: {
    account: string;
    password: string;
  };
}> = ({ form }) => {
  const dispatch = useAppDispatch();
  const onSubmit = () => console.log(form);

  return (
    <RegisterFormContainer component="form">
      <Typography variant="h4" align="center" sx={{ mb: "1rem" }}>
        Sign in
      </Typography>
      <Typography variant="subtitle1" align="center" sx={{ mb: "1rem" }}>
        Join the hot topic and have fun!
      </Typography>
      <TextInput
        icon={<AccountCircle sx={{ mr: 1, mb: "1rem" }} />}
        placeholder="account"
        sx={{ mb: "1rem" }}
        value={form.account}
        onChange={(e) =>
          dispatch(setForm({ ...form, account: e.target.value }))
        }
      />
      <TextInput
        icon={<LockIcon sx={{ mr: 1 }} />}
        placeholder="password"
        type="password"
        value={form.password}
        onChange={(e) =>
          dispatch(setForm({ ...form, password: e.target.value }))
        }
      />
      <HorizontalEnd>
        <Button>Forgot password?</Button>
      </HorizontalEnd>
      <Button
        variant="contained"
        sx={{ width: "100%", mb: "1rem" }}
        onClick={onSubmit}
      >
        Sign in
      </Button>
      <Divider />
      Need an account?
      <Button>
        <Link href="/signup">Sign up</Link>
      </Button>
    </RegisterFormContainer>
  );
};

const SignIn: NextPage = () => {
  const { form } = useAppSelector((state) => state.signin);

  return (
    <Box>
      <Head>
        <title>SignIn | yuchi forum</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <MainContainer component="main">
        <Header />
        <SignInForm form={form} />
      </MainContainer>
    </Box>
  );
};

export default SignIn;
